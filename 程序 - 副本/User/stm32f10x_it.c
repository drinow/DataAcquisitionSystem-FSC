/**
  * @brief  This function handles SysTick Handler.
  * @param  None
  * @retval None
  */
volatile u32 SysCnt=0;
volatile u8 SendMotec=0,SendADC=0,SendAtt=0,SendGPS=0;
extern void Protection(void);
void SysTick_Handler(void)
{
	
	SysCnt++;
	//œ‘ æ
	
	//500hz
	if(SysCnt%2==0)    {;}
	
	//200hz
	if(SysCnt%5   ==0) {SendAtt=1;SendADC=1;SendMotec=1;}

	//100hz
	if(SysCnt%10  ==0) {;}

	//50hz
	if(SysCnt%20  ==0) {;}

	//10hz
	if(SysCnt%100  ==0) {SendGPS=1;}
	
	//1hz
	if(SysCnt%1000==0) {Protection();}

	//LED ÷∏ æ
	LED_STATE_Fun();
}

